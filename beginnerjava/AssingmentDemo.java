package beginnerjava;

public class AssingmentDemo
{
    public static void main(String[] args)
    {
        int x =3;
        int y= 2;
        
        x+=y;  //x+y = 5.
        System.out.println("X = "+x);
        
        x-=y;  //x+y = 5.
        System.out.println("X = "+x);
        
        x *=y;  //x+y = 5.
        System.out.println("X = "+x);
        
        x /=y;  //x+y = 5.
        System.out.println("X = "+x);
        
        x  %=y;  //x+y = 5.
        System.out.println("X = "+x);
        
    }
    
}
