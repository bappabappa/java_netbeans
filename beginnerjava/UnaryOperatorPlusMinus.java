
package beginnerjava;

public class UnaryOperatorPlusMinus {
    
    public static void main(String[] args) {
        
        int x =-10;
        int result;
        
        result =+x; //  -10 print hbee
        System.out.println("Result is = "+result);
        
        result =-x; //  +10 print hbeee
        System.out.println("Result is = "+result);
        
    }
    
}
