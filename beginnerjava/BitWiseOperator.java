package beginnerjava;

public class BitWiseOperator
{
    public static void main(String[] args)
    {
        int a =32;
        int b =12;
        int c ;
        
        c = a&b; // a bitwise and b
        System.out.println("a&b = " +c);
        
        c = a|b; // a bitwise and b
        System.out.println("a|b = " +c);
        
        c = a^b; // a bitwise and b
        System.out.println("a^b = " +c);
        
    }
    
}
